import java.util.*;
public class BitwiseOperator {
    public static void main(String[] args){
        Scanner sc =new Scanner(System.in);
        int a = 5;// 101
        int b = 6;// 110
        System.out.println(a&b);

        System.out.println(a|b);
        
        System.out.println(a^b);

        System.out.println(a<<2);

        System.out.println(6>>1);
    }
}
