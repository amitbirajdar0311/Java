public class Switch {
    public static void main(String[] args) {
        char ch = 'a';

        switch (ch) {
            case 'a':
                System.out.println("Amit");
                break;
            case 'b':
                System.out.println("bhumi");
                break;
            case 'c':
                System.out.println("chitra");
                break;
            default:
                System.out.println("Invalid ");
        }

    }
}